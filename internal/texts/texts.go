func Reason() string {
	return genericReasons[rand.Intn(len(genericReasons))]
}

func ReasonForUser(username string, displayName string) string {
	name := strings.TrimSpace(displayName)
	if name == "" {
		name = strings.TrimSpace(username)
	}
	if name == "" {
		return Reason()
	}

	identity := normalizeIdentity(username + " " + displayName)
	for _, set := range personalReasonSets {
		for _, key := range set.Keys {
			if strings.Contains(identity, normalizeIdentity(key)) {
				return fillName(nextPersonalReason(set), name)
			}
		}
	}

	return ReasonForName(name)
}

func ReasonForName(name string) string {
	name = strings.TrimSpace(name)
	if name == "" {
		return Reason()
	}
	if rand.Intn(100) < 20 {
		return fillName(legendaryReasons[rand.Intn(len(legendaryReasons))], name)
	}
	if rand.Intn(100) < 70 {
		return fillName(nameReasons[rand.Intn(len(nameReasons))], name)
	}
	return Reason()
}

func fillName(template string, name string) string {
	return strings.ReplaceAll(template, "{name}", name)
}

func normalizeIdentity(s string) string {
	s = strings.ToLower(strings.TrimSpace(s))
	s = strings.TrimPrefix(s, "@")
	s = strings.ReplaceAll(s, "ё", "е")
	return s
}
