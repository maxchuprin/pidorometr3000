var personalReasonState = struct {
	sync.Mutex
	bags map[string][]int
}{
	bags: make(map[string][]int),
}

func nextPersonalReason(set personReasonSet) string {
	personalReasonState.Lock()
	defer personalReasonState.Unlock()

	id := set.Keys[0]
	bag := personalReasonState.bags[id]
	if len(bag) == 0 {
		bag = rand.Perm(len(set.Reasons))
	}
	index := bag[len(bag)-1]
	personalReasonState.bags[id] = bag[:len(bag)-1]
	return set.Reasons[index]
}

